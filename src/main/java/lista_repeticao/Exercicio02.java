package lista_repeticao;

public class Exercicio02 {

	public static void main(String[] args) {
		
		for (int contador = 1; contador <= 10; contador++) {
			
			System.out.println(contador);
			
		}
		
		System.out.println("---");
		
		for (int contador = 10; contador > 0; contador--) {
			
			System.out.println(contador);
			
		}
		
	}

}
